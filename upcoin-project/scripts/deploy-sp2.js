// Crear una instancia del contrato
const contract = new web3.eth.Contract(abi, contractAddress);

// Función para transferir UPCoin
async function transferUPCoin(toAddress, amount) {
  const decimals = 18;  // Nuestro token es de 18 decimales

  // Convertir el monto a la cantidad correcta con decimales
  // TODO: Acabar de mirar por que toBN nomlo reconoce...
  //const tokenAmount = web3.utils.toBN(amount * 10 ** decimals); 
  const tokenAmount = web3.utils.toWei(amount, "ether"); // Convierte a Wei

  // Crear los datos de la transacción (llamando a la función "transfer" del contrato ERC-20)
  const tx = contract.methods.transfer(toAddress, tokenAmount);

  // Obtener el gas estimado para la transacción
  const gas = await tx.estimateGas({ from: account.address });

  // Obtener el gas price actual de la red
  const gasPrice = await web3.eth.getGasPrice();

  // Construir la transacción
  const txData = {
    from: account.address,
    to: contractAddress,
    data: tx.encodeABI(),
    gas,
    gasPrice
  };

  // Firmar la transacción con la clave privada
  const signedTx = await web3.eth.accounts.signTransaction(txData, privateKey);

  // Enviar la transacción firmada
  const receipt = await web3.eth.sendSignedTransaction(signedTx.rawTransaction);
  console.log('Transacción enviada: ', receipt.transactionHash);
  console.log('Transferencia exitosa');
}

